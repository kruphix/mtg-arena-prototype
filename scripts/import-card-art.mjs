// Imports hand-generated card art from art-source/ into public/cards/.
// Run with: node --experimental-strip-types scripts/import-card-art.mjs
//
// Drop an image per card into art-source/, named after the card id or name
// (e.g. emberWhelp.png, "Ember Whelp.jpg", ember-whelp.webp all match the
// emberWhelp card). Supported formats: png, jpg, jpeg, webp. Re-run any time
// you add or replace files there. Cards with no matching file keep their
// existing generated SVG placeholder.
//
// Matched images are downscaled/cropped to ART_SIZE and re-encoded as
// compressed webp via sharp, regardless of source resolution or aspect
// ratio — the on-screen art box is small (see .card__art in index.css), so
// there's no reason to ship multi-megabyte source renders to the browser.
import { mkdirSync, readdirSync, existsSync, unlinkSync, writeFileSync } from 'node:fs';
import { dirname, join, extname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';
import sharp from 'sharp';
import { CARDS } from '../src/engine/cards.ts';

const __dirname = dirname(fileURLToPath(import.meta.url));
const sourceDir = join(__dirname, '..', 'art-source');
const outDir = join(__dirname, '..', 'public', 'cards');
const artMapPath = join(__dirname, '..', 'src', 'ui', 'cardArt.ts');
const VALID_EXT = ['png', 'jpg', 'jpeg', 'webp'];
const ART_SIZE = { width: 360, height: 225 };

mkdirSync(sourceDir, { recursive: true });
mkdirSync(outDir, { recursive: true });

function normalize(s) {
  return s.toLowerCase().replace(/[^a-z0-9]/g, '');
}

const cardIds = Object.keys(CARDS);
const sourceFiles = existsSync(sourceDir) ? readdirSync(sourceDir) : [];

const matches = new Map(); // card id -> source filename
for (const file of sourceFiles) {
  const ext = extname(file).slice(1).toLowerCase();
  if (!VALID_EXT.includes(ext)) continue;
  const normFile = normalize(basename(file, extname(file)));
  const matchId = cardIds.find(
    (id) => normalize(id) === normFile || normalize(CARDS[id].name) === normFile
  );
  if (matchId) matches.set(matchId, file);
}

const artMap = {};
let replaced = 0;
for (const id of cardIds) {
  const srcFile = matches.get(id);
  if (srcFile) {
    const destName = `${id}.webp`;
    await sharp(join(sourceDir, srcFile))
      .resize(ART_SIZE.width, ART_SIZE.height, { fit: 'cover', position: 'centre' })
      .webp({ quality: 80 })
      .toFile(join(outDir, destName));
    // Clean up stale art in other formats (e.g. a previous import, or the old placeholder).
    for (const staleExt of [...VALID_EXT, 'svg']) {
      if (staleExt === 'webp') continue; // that's destName, just written above
      const stalePath = join(outDir, `${id}.${staleExt}`);
      if (existsSync(stalePath)) unlinkSync(stalePath);
    }
    artMap[id] = `/cards/${destName}`;
    replaced += 1;
  } else {
    artMap[id] = `/cards/${id}.svg`;
  }
}

const tsOut = `// Generated by scripts/import-card-art.mjs — do not edit by hand.
// Maps each card id to its art file (custom art if imported, else the SVG placeholder).
export const CARD_ART: Record<string, string> = ${JSON.stringify(artMap, null, 2)};
`;
writeFileSync(artMapPath, tsOut);

console.log(`Imported ${replaced}/${cardIds.length} custom card art files from ${sourceDir}`);
const missing = cardIds.filter((id) => !matches.has(id));
if (missing.length) {
  console.log(`Still using placeholders for (${missing.length}): ${missing.join(', ')}`);
}
